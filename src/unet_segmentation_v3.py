def polyline_length(points):
    """
    points: list of [x, y]
    """
    if points is None or len(points) < 2:
        return 0.0

    length = 0.0

    for i in range(len(points) - 1):
        x1, y1 = points[i]
        x2, y2 = points[i + 1]
        length += math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)

    return float(length)

