def count_skeleton_endpoints(skel):
    """
    Count endpoints in a binary skeleton.
    Endpoint = skeleton pixel with exactly one skeleton neighbor.
    """
    skel = skel.astype(np.uint8)

    kernel = np.array([
        [1, 1, 1],
        [1, 10, 1],
        [1, 1, 1]
    ], dtype=np.uint8)

    neighbor_count = ndi.convolve(skel, kernel, mode="constant", cval=0)

    # center pixel contributes 10, endpoint has 1 neighbor => value = 11
    endpoints = np.logical_and(skel == 1, neighbor_count == 11)

    return int(endpoints.sum())


def skeleton_readiness_stats(mask):
    """
    mask: binary numpy mask
    """
    mask_bool = mask.astype(bool)

    cc = label(mask_bool)
    num_components = int(cc.max())

    skel = skeletonize(mask_bool)
    skel_cc = label(skel)
    num_skeleton_components = int(skel_cc.max())

    skeleton_pixels = int(skel.sum())
    endpoints = count_skeleton_endpoints(skel)

    return {
        "num_components": num_components,
        "num_skeleton_components": num_skeleton_components,
        "skeleton_pixels": skeleton_pixels,
        "skeleton_endpoints": endpoints
    }

