"""14. Loss funcion

Vì mask cây đổ chỉ chiếm dưới 5–10% ảnh, cần kết hợp BCE và Dice
"""

bce_loss = nn.BCEWithLogitsLoss()
dice_loss = smp.losses.DiceLoss(
    mode="binary",
    from_logits=True
)

def loss_fn(logits, masks):
    loss_bce = bce_loss(logits, masks)
    loss_dice = dice_loss(logits, masks)
    return loss_bce + loss_dice

"""15. Metrics: IoU, Dice, Precision, Recall

Ở giai đoạn 5, tạm dùng threshold = 0.5 để theo dõi trong training. Sau này ở giai đoạn 6–7 sẽ threshold sweep.
"""

def compute_metrics(logits, masks, threshold=0.5, eps=1e-7):
    """
    logits: raw output from model, shape [B,1,H,W]
    masks : ground truth, shape [B,1,H,W]
    """
    probs = torch.sigmoid(logits)
    preds = (probs > threshold).float()

    masks = masks.float()

    tp = (preds * masks).sum(dim=(1, 2, 3))
    fp = (preds * (1 - masks)).sum(dim=(1, 2, 3))
    fn = ((1 - preds) * masks).sum(dim=(1, 2, 3))

    precision = (tp + eps) / (tp + fp + eps)
    recall = (tp + eps) / (tp + fn + eps)

    dice = (2 * tp + eps) / (2 * tp + fp + fn + eps)
    iou = (tp + eps) / (tp + fp + fn + eps)

    return {
        "iou": iou.mean().item(),
        "dice": dice.mean().item(),
        "precision": precision.mean().item(),
        "recall": recall.mean().item()
    }

