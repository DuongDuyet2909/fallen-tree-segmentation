# =========================

def predict_single_image(image_path, model, threshold=0.5):
    original_bgr = cv2.imread(image_path)

    if original_bgr is None:
        raise ValueError(f"Không đọc được ảnh: {image_path}")

    original_rgb = cv2.cvtColor(original_bgr, cv2.COLOR_BGR2RGB)
    original_h, original_w = original_rgb.shape[:2]

    transformed = infer_transform(image=original_rgb)
    image_tensor = transformed["image"].unsqueeze(0).to(DEVICE)

    with torch.no_grad():
        logits = model(image_tensor)
        prob = torch.sigmoid(logits)
        prob = prob.squeeze().cpu().numpy()

    pred_mask = (prob >= threshold).astype(np.uint8) * 255

    # Resize mask về kích thước ảnh gốc
    pred_mask_original_size = cv2.resize(
        pred_mask,
        (original_w, original_h),
        interpolation=cv2.INTER_NEAREST
    )

    return original_rgb, pred_mask_original_size


# =========================
# 6. Hàm tạo ảnh overlay
# =========================

def create_overlay(image_rgb, mask, alpha=0.45):
    overlay = image_rgb.copy()

    red_mask = np.zeros_like(image_rgb)
    red_mask[:, :, 0] = 255

    mask_bool = mask > 0

    overlay[mask_bool] = (
        image_rgb[mask_bool] * (1 - alpha) + red_mask[mask_bool] * alpha
    ).astype(np.uint8)

    return overlay


