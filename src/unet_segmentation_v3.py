"""8. Dataset class

Cell này đọc ảnh RGB và mask nhị phân 0/255. Mask sẽ được chuyển về 0/1 để train.
"""

class FallenTreeDataset(Dataset):
    def __init__(self, image_dir, mask_dir, transform=None):
        self.image_dir = image_dir
        self.mask_dir = mask_dir
        self.transform = transform

        self.image_files = sorted([
            f for f in os.listdir(image_dir)
            if f.lower().endswith((".jpg", ".jpeg", ".png", ".tif", ".tiff"))
        ])

        self.mask_files = sorted([
            f for f in os.listdir(mask_dir)
            if f.lower().endswith((".png", ".jpg", ".jpeg", ".tif", ".tiff"))
        ])

        self.mask_map = {
            os.path.splitext(f)[0]: f for f in self.mask_files
        }

        valid_image_files = []
        missing_masks = []

        for img_name in self.image_files:
            stem = os.path.splitext(img_name)[0]
            if stem in self.mask_map:
                valid_image_files.append(img_name)
            else:
                missing_masks.append(img_name)

        self.image_files = valid_image_files

        if len(missing_masks) > 0:
            print(f"Warning: {len(missing_masks)} images do not have corresponding masks.")
            print("Example missing:", missing_masks[:5])

    def __len__(self):
        return len(self.image_files)

    def __getitem__(self, idx):
        img_name = self.image_files[idx]
        stem = os.path.splitext(img_name)[0]
        mask_name = self.mask_map[stem]

        img_path = os.path.join(self.image_dir, img_name)
        mask_path = os.path.join(self.mask_dir, mask_name)

        image = cv2.imread(img_path)
        if image is None:
            raise FileNotFoundError(f"Cannot read image: {img_path}")

        image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)

        mask = cv2.imread(mask_path, cv2.IMREAD_GRAYSCALE)
        if mask is None:
            raise FileNotFoundError(f"Cannot read mask: {mask_path}")

        # Convert mask 0/255 to 0/1
        mask = (mask > 127).astype("float32")

        if self.transform is not None:
            augmented = self.transform(image=image, mask=mask)
            image = augmented["image"]
            mask = augmented["mask"]

        # mask shape: H,W -> 1,H,W
        mask = mask.unsqueeze(0).float()

        return image, mask

