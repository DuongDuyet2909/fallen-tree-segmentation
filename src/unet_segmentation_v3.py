def count_skeleton_neighbors(skeleton):
    """
    Count 8-neighbors for each skeleton pixel.
    skeleton: binary numpy mask, 0/1 or bool
    """
    skel = skeleton.astype(np.uint8)

    kernel = np.array([
        [1, 1, 1],
        [1, 0, 1],
        [1, 1, 1]
    ], dtype=np.uint8)

    neighbor_count = ndi.convolve(skel, kernel, mode="constant", cval=0)

    return neighbor_count


def detect_endpoints_and_branchpoints(skeleton):
    """
    Endpoint: skeleton pixel with exactly 1 neighbor.
    Branch point: skeleton pixel with >= 3 neighbors.
    """
    skel = skeleton.astype(bool)
    neighbor_count = count_skeleton_neighbors(skel)

    endpoints = np.logical_and(skel, neighbor_count == 1)
    branchpoints = np.logical_and(skel, neighbor_count >= 3)

    return endpoints, branchpoints, neighbor_count

"""5. Tính chiều dài segment có tính đường chéo √2

Cách này tính chiều dài bằng cách lần theo các pixel trong từng segment. Vì sau khi loại branch point, mỗi connected component thường là một đoạn không phân nhánh.
"""

def get_neighbors_8(y, x, shape):
    """
    Return valid 8-neighbors of pixel (y, x).
    """
    h, w = shape
    neighbors = []

    for dy in [-1, 0, 1]:
        for dx in [-1, 0, 1]:
            if dy == 0 and dx == 0:
                continue

            ny, nx = y + dy, x + dx

            if 0 <= ny < h and 0 <= nx < w:
                neighbors.append((ny, nx))

    return neighbors


def segment_length_diagonal_aware(segment_mask):
    """
    Estimate skeleton segment length.
    Horizontal/vertical step = 1
    Diagonal step = sqrt(2)

    For an unbranched skeleton component, length can be approximated
    by summing graph edges once.
    """
    seg = segment_mask.astype(bool)
    coords = np.argwhere(seg)

    if len(coords) <= 1:
        return 0.0

    coord_set = set((int(y), int(x)) for y, x in coords)

    length = 0.0
    visited_edges = set()

    for y, x in coord_set:
        for ny, nx in get_neighbors_8(y, x, seg.shape):
            if (ny, nx) not in coord_set:
                continue

            edge = tuple(sorted([(y, x), (ny, nx)]))

            if edge in visited_edges:
                continue

            visited_edges.add(edge)

            dy = abs(ny - y)
            dx = abs(nx - x)

            if dy == 1 and dx == 1:
                length += math.sqrt(2)
            else:
                length += 1.0

    return float(length)

"""6. Lấy endpoints của từng segment"""

def get_segment_endpoints(segment_mask):
    """
    Return endpoints of a single segment.
    If no clear endpoints are found, return farthest two pixels.
    Coordinates are returned as (x, y).
    """
    seg = segment_mask.astype(bool)
    endpoints, _, _ = detect_endpoints_and_branchpoints(seg)

    ep_coords = np.argwhere(endpoints)

    if len(ep_coords) >= 2:
        # If more than 2 endpoints due to tiny artifacts, choose farthest pair
        coords = ep_coords
    else:
        coords = np.argwhere(seg)

    if len(coords) == 0:
        return None, None

    if len(coords) == 1:
        y, x = coords[0]
        return (int(x), int(y)), (int(x), int(y))

    # Find farthest pair
    max_dist = -1
    p1, p2 = None, None

    for i in range(len(coords)):
        y1, x1 = coords[i]
        for j in range(i + 1, len(coords)):
            y2, x2 = coords[j]
            dist = (y1 - y2) ** 2 + (x1 - x2) ** 2

            if dist > max_dist:
                max_dist = dist
                p1 = (int(x1), int(y1))
                p2 = (int(x2), int(y2))

    return p1, p2

"""7. Kiểm tra segment có chạm biên ảnh không

Ta không xóa segment chạm biên, vì cây có thể đi qua mép tile. Nhưng vẫn lưu thông tin này vào CSV.
"""

def touches_image_border(segment_mask):
    seg = segment_mask.astype(bool)

    if seg.sum() == 0:
        return False

    top = seg[0, :].any()
    bottom = seg[-1, :].any()
    left = seg[:, 0].any()
    right = seg[:, -1].any()

    return bool(top or bottom or left or right)

"""# V.2 Hàm skeletonize và tách segment

8. Extract segments từ binary mask
"""

def extract_skeleton_segments_from_mask(
    binary_mask,
    min_segment_length_px=20,
    count_diagonal_as_sqrt2=True,
    remove_branch_points=True,
    keep_border_touching=True
):
    """
    Input:
        binary_mask: numpy binary mask 0/1 or bool

    Output:
        skeleton
        endpoints
        branchpoints
        segment_label_mask
        segment_records
    """
    mask_bool = binary_mask.astype(bool)

    # 1. Skeletonization
    skeleton = skeletonize(mask_bool)

    # 2. Detect endpoints and branch points
    endpoints, branchpoints, neighbor_count = detect_endpoints_and_branchpoints(skeleton)

    # 3. Remove branch points before extracting unbranched segments
    if remove_branch_points:
        skeleton_for_segments = skeleton.copy()
        skeleton_for_segments[branchpoints] = False
    else:
        skeleton_for_segments = skeleton.copy()

    # 4. Connected components on skeleton without branch points
    labeled_segments = label(skeleton_for_segments, connectivity=2)

    segment_label_mask = np.zeros_like(labeled_segments, dtype=np.int32)
    segment_records = []

    new_segment_id = 1

    for region in regionprops(labeled_segments):
        old_label = region.label

        segment_mask = labeled_segments == old_label

        if count_diagonal_as_sqrt2:
            length_px = segment_length_diagonal_aware(segment_mask)
        else:
            length_px = float(segment_mask.sum())

        num_pixels = int(segment_mask.sum())

        p1, p2 = get_segment_endpoints(segment_mask)
        border_touch = touches_image_border(segment_mask)

        # Filter short segments
        keep = length_px >= min_segment_length_px

        # Keep or remove border segments based on configuration
        if (not keep_border_touching) and border_touch:
            keep = False

        if not keep:
            continue

        segment_label_mask[segment_mask] = new_segment_id

        record = {
            "segment_id": new_segment_id,
            "length_px": length_px,
            "num_pixels": num_pixels,
            "endpoint_1_x": p1[0] if p1 is not None else None,
            "endpoint_1_y": p1[1] if p1 is not None else None,
            "endpoint_2_x": p2[0] if p2 is not None else None,
            "endpoint_2_y": p2[1] if p2 is not None else None,
            "touches_border": border_touch
        }

        segment_records.append(record)

        new_segment_id += 1

    return {
        "skeleton": skeleton.astype(np.uint8),
        "endpoints": endpoints.astype(np.uint8),
        "branchpoints": branchpoints.astype(np.uint8),
        "segment_label_mask": segment_label_mask,
        "segment_records": segment_records,
        "num_branchpoints": int(branchpoints.sum()),
        "num_endpoints": int(endpoints.sum()),
        "num_segments_after_filter": len(segment_records),
        "num_segments_before_filter": int(labeled_segments.max())
    }

