def mask_to_binary(mask_path, image_size=512):
    mask = cv2.imread(mask_path, cv2.IMREAD_GRAYSCALE)
    if mask is None:
        return None

    mask = cv2.resize(mask, (image_size, image_size), interpolation=cv2.INTER_NEAREST)
