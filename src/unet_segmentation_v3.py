def point_to_polyline_min_distance(points, polyline):
    """
    Approximate distance from each point in points to nearest point in polyline vertices.
    For simplicity, uses vertex distance. For publication-grade, use segment distance.
    """
    if len(points) == 0 or len(polyline) == 0:
        return np.array([])

    points = np.array(points, dtype=np.float32)
    polyline = np.array(polyline, dtype=np.float32)

    dist = cdist(points, polyline)
    return dist.min(axis=1)


def gt_coverage_by_pred(gt_polyline, pred_points, distance_threshold_px=10):
    """
    Coverage = fraction of GT polyline vertices close to predicted object points.
    """
    if len(gt_polyline) == 0 or len(pred_points) == 0:
        return 0.0

    gt_points = np.array(gt_polyline, dtype=np.float32)
    pred_points = np.array(pred_points, dtype=np.float32)

    dist = cdist(gt_points, pred_points)
    min_dist = dist.min(axis=1)

    coverage = np.mean(min_dist <= distance_threshold_px)

    return float(coverage)

