def evaluate_object_level(
    gt_df,
    pred_df,
    split_name="test",
    distance_threshold_px=10,
    dr_thresholds=(0.25, 0.50)
):
    gt_split = gt_df[gt_df["split"] == split_name].copy()
    pred_split = pred_df[pred_df["split"] == split_name].copy()

    eval_records = []

    image_indices = sorted(gt_split["image_index"].unique())

    for image_index in image_indices:
        img_gt = gt_split[gt_split["image_index"] == image_index].copy()
        img_pred = pred_split[pred_split["image_index"] == image_index].copy()

        candidate_matches = []

        for _, gt_row in img_gt.iterrows():
            gt_id = int(gt_row["gt_id"])
            gt_polyline = gt_row["polyline"]
            gt_len = float(gt_row["gt_length_px"])

            for _, pred_row in img_pred.iterrows():
                pred_id = int(pred_row["pred_id"])
                pred_points = pred_row["points"]
                pred_len = float(pred_row["pred_length_px"])

                coverage = gt_coverage_by_pred(
                    gt_polyline=gt_polyline,
                    pred_points=pred_points,
                    distance_threshold_px=distance_threshold_px
                )

                length_error_px = abs(pred_len - gt_len)
                relative_length_error = length_error_px / gt_len if gt_len > 0 else np.nan

                candidate_matches.append({
                    "image_index": int(image_index),
                    "gt_id": gt_id,
                    "pred_id": pred_id,
                    "coverage": coverage,
                    "gt_length_px": gt_len,
                    "pred_length_px": pred_len,
                    "length_error_px": length_error_px,
                    "relative_length_error": relative_length_error
                })

        match_df = pd.DataFrame(candidate_matches)

        # Greedy matching by coverage
        matched_gt = set()
        matched_pred = set()
        accepted_matches = []

        if len(match_df) > 0:
            match_df = match_df.sort_values("coverage", ascending=False)

            for _, row in match_df.iterrows():
                gt_id = int(row["gt_id"])
                pred_id = int(row["pred_id"])

                if gt_id in matched_gt or pred_id in matched_pred:
                    continue

                # Accept match if at least DR25 threshold
                if row["coverage"] >= 0.25:
                    accepted_matches.append(row.to_dict())
                    matched_gt.add(gt_id)
                    matched_pred.add(pred_id)

        tp = len(accepted_matches)
        fp = len(img_pred) - tp
        fn = len(img_gt) - tp

        dr25 = 0
        dr50 = 0

        for m in accepted_matches:
            if m["coverage"] >= 0.25:
                dr25 += 1
            if m["coverage"] >= 0.50:
                dr50 += 1

        gt_count = len(img_gt)

        dr25_rate = dr25 / gt_count if gt_count > 0 else np.nan
        dr50_rate = dr50 / gt_count if gt_count > 0 else np.nan

        precision = tp / (tp + fp) if (tp + fp) > 0 else np.nan
        recall = tp / (tp + fn) if (tp + fn) > 0 else np.nan
        f1 = 2 * precision * recall / (precision + recall) if precision + recall > 0 else np.nan

        if len(accepted_matches) > 0:
            mean_length_error = np.mean([m["length_error_px"] for m in accepted_matches])
