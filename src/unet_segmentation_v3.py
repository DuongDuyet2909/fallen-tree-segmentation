def get_neighbors(p, skel_set):
    x, y = p
    neighbors = []

    for dx in [-1, 0, 1]:
        for dy in [-1, 0, 1]:
            if dx == 0 and dy == 0:
                continue

            q = (x + dx, y + dy)
            if q in skel_set:
                neighbors.append(q)

    return neighbors


def bfs_farthest(start, skel_set):
    visited = {start}
    parent = {start: None}
    queue = deque([start])
    farthest = start

    while queue:
        current = queue.popleft()
        farthest = current

        for nb in get_neighbors(current, skel_set):
            if nb not in visited:
                visited.add(nb)
                parent[nb] = current
                queue.append(nb)

    return farthest, parent


def longest_skeleton_path(skeleton_component):
    ys, xs = np.where(skeleton_component > 0)

    if len(xs) == 0:
        return []

    skel_points = list(zip(xs, ys))
    skel_set = set(skel_points)

    degrees = {
        p: len(get_neighbors(p, skel_set))
        for p in skel_points
    }

    endpoints = [p for p, d in degrees.items() if d == 1]

    if len(endpoints) > 0:
        start = endpoints[0]
    else:
        start = skel_points[0]

    p1, _ = bfs_farthest(start, skel_set)
    p2, parent = bfs_farthest(p1, skel_set)

    path = []
    cur = p2

    while cur is not None:
        path.append([int(cur[0]), int(cur[1])])
        cur = parent[cur]

    path = path[::-1]
    return path


