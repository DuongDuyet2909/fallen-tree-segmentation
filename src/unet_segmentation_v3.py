def postprocess_mask(mask, config):
    """
    mask: binary numpy mask, values 0/1 or bool
    config: dictionary
    """
    m = mask.astype(bool)

    # 1. Remove small objects
    min_area = config.get("min_area", None)
    if min_area is not None and min_area > 0:
        m = remove_small_objects(m, min_size=min_area)

    # 2. Opening
    opening_radius = config.get("opening_radius", 0)
    opening_shape = config.get("opening_shape", "square")

    if opening_radius > 0:
        if opening_shape == "disk":
            selem = disk(opening_radius)
        else:
            k = 2 * opening_radius + 1
            selem = square(k)

        m = binary_opening(m, selem)

    # 3. Closing
    # Be careful: closing can worsen over-connected masks.
    closing_radius = config.get("closing_radius", 0)
    closing_shape = config.get("closing_shape", "square")

    if closing_radius > 0:
        if closing_shape == "disk":
            selem = disk(closing_radius)
        else:
            k = 2 * closing_radius + 1
            selem = square(k)

        m = binary_closing(m, selem)

    # 4. Fill small holes
    hole_area = config.get("hole_area", None)
    if hole_area is not None and hole_area > 0:
        m = remove_small_holes(m, area_threshold=hole_area)

    # 5. Shape filtering
    if config.get("shape_filter", False):
        m = filter_by_shape(
            m,
            min_area=config.get("shape_min_area", 50),
            min_major_axis=config.get("min_major_axis", 20),
            min_aspect_ratio=config.get("min_aspect_ratio", 2.0),
            keep_small_if_area_over=config.get("keep_large_area", 300)
        )

    return m.astype(np.uint8)

