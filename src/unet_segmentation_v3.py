"""# VI.1 Hàm hình học cơ bản

4. Vector, góc, khoảng cách
"""

def euclidean_distance(p1, p2):
    """
    p1, p2: (x, y)
    """
    return float(math.sqrt((p1[0] - p2[0]) ** 2 + (p1[1] - p2[1]) ** 2))


def normalize_vector(v):
    v = np.array(v, dtype=np.float32)
    norm = np.linalg.norm(v)

    if norm < 1e-6:
        return None

    return v / norm


def angle_between_vectors_deg(v1, v2):
    """
    Return angle between vectors in degrees, range 0–180.
    """
    v1 = normalize_vector(v1)
    v2 = normalize_vector(v2)

    if v1 is None or v2 is None:
        return None

    dot = np.clip(np.dot(v1, v2), -1.0, 1.0)
    angle = math.degrees(math.acos(dot))

    return float(angle)


def acute_angle_deg(angle):
    """
    Convert angle 0–180 to acute alignment angle 0–90.
    Useful because opposite directions can still represent the same stem axis.
    """
    if angle is None:
        return None

    return min(angle, 180.0 - angle)

"""5. Probability evidence along gap

Dùng probability map để kiểm tra đoạn nối giữa hai endpoin có tí hiệu cây đổ không
"""

def mean_probability_along_line(prob_map, p1, p2, line_width=1):
    """
    prob_map: numpy [H, W], values 0–1
    p1, p2: (x, y)
    """
    x1, y1 = int(round(p1[0])), int(round(p1[1]))
    x2, y2 = int(round(p2[0])), int(round(p2[1]))

    h, w = prob_map.shape

    y1 = np.clip(y1, 0, h - 1)
    y2 = np.clip(y2, 0, h - 1)
    x1 = np.clip(x1, 0, w - 1)
    x2 = np.clip(x2, 0, w - 1)

    rr, cc = line(y1, x1, y2, x2)

    values = []

    for r, c in zip(rr, cc):
        for dy in range(-line_width, line_width + 1):
            for dx in range(-line_width, line_width + 1):
                nr, nc = r + dy, c + dx
                if 0 <= nr < h and 0 <= nc < w:
                    values.append(prob_map[nr, nc])

    if len(values) == 0:
        return 0.0

    return float(np.mean(values))

