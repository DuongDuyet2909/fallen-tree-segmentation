def run_reconnect_on_split(
    split_name,
    segment_df,
    endpoint_df,
    probs,
    config_name,
    config,
    segment_mask_dir,
    reconstructed_mask_dir
):
    all_candidates = []
    all_selected = []
    summary_records = []

    image_indices = sorted(segment_df[segment_df["split"] == split_name]["image_index"].unique())

    for image_index in tqdm(image_indices, desc=f"Reconnect {split_name} - {config_name}"):

        image_seg_df = segment_df[
            (segment_df["split"] == split_name) &
            (segment_df["image_index"] == image_index)
        ].copy()

        image_ep_df = endpoint_df[
            (endpoint_df["split"] == split_name) &
            (endpoint_df["image_index"] == image_index)
        ].copy()

        if len(image_seg_df) == 0:
            summary_records.append({
                "split": split_name,
                "image_index": image_index,
                "config_name": config_name,
                "num_segments_before": 0,
                "num_candidate_connections": 0,
                "num_selected_connections": 0,
                "num_groups_after": 0,
                "total_length_before_px": 0.0,
                "estimated_total_length_after_px": 0.0
            })
            continue

        prob_map = probs[int(image_index), 0].numpy()

        candidates_df = generate_reconnect_candidates_for_image(
            image_endpoint_df=image_ep_df,
            prob_map=prob_map,
            config=config
        )

        if len(candidates_df) > 0:
            candidates_df["config_name"] = config_name
            all_candidates.append(candidates_df)

        selected_df = select_connections_greedy(candidates_df, config)

        if len(selected_df) > 0:
            selected_df["config_name"] = config_name
            all_selected.append(selected_df)

        groups = build_segment_groups(image_seg_df, selected_df)

        total_length_before = float(image_seg_df["length_px"].sum())

        if len(selected_df) > 0:
            total_gap_added = float(selected_df["gap_px"].sum())
